'use strict';
const number1 = 5;         // Definition einer Ganzzahl
const number2 = 0.5;       // Definition einer Kommazahl
const number3 = -22;       // Definition einer negativen Ganzzahl
const number4 = -0.9;      // Definition einer negativen Kommazahl
const number5 = 12_300;    // Trennzeichen bei Dezimaldarstellung
const number6 = 1_000_000; // Trennzeichen bei Dezimaldarstellung

console.log(number1);    // 5
console.log(number2);    // 0.5
console.log(number3);    // -22
console.log(number4);    // -0-9
console.log(number5);    // 12300
console.log(number6);    // 1000000
